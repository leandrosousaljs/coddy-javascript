/* Challenge

Create a function expression named calculateVolume that takes three arguments, length, width, and height, and returns the volume of a rectangular prism. The function should be assigned to a variable named calculateVolume. */

// Type your code below

let calculateVolume = function (l, w, h) {
  return l * w * h;
};

